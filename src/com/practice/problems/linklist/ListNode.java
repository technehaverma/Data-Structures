package com.practice.problems.linklist;

public class ListNode {
	public int data;
	public ListNode next;

	public ListNode() {
		super();
	}

	public ListNode(int data) {
		super();
		this.data = data;
	
	}

	public int getData() {
		return data;
	}

	public ListNode getNext() {
		return next;
	}

	public void setData(int data) {
		this.data = data;
	}

	public void setNext(ListNode next) {
		this.next = next;
	}

}
